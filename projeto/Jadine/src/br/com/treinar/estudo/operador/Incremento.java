package br.com.treinar.estudo.operador;

public class Incremento {

	public static void main(String[] args) {
		
		int cont = 0;
		System.out.println(cont);
		
		cont += 2;
		System.out.println(cont);

		cont += 2;
		System.out.println(cont);
		
		cont += 2;
		System.out.println(cont);
	
		cont += 2;
		System.out.println(cont);
		
		cont += 2;
		System.out.println(cont);
		
	}
}
